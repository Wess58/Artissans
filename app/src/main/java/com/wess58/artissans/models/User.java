package com.wess58.artissans.models;

public class User {
    public String firstName;
    public String lastName;
    public String phone;
    public String email;

    public User(){


    }

    public User(String firstName, String lastName, String phone ){
        this.firstName = firstName;
        this.lastName = lastName;
        this.phone = phone;
        this.email = email;
    }

}
